package CodingBat;

public class MultydimentionalArrays {
    public static void main(String[] args) {
        int [][]nums={{1,2,3,4}, {2,3,4,6}};
        for (int i=0;i<2;i++){
            for(int j=0;j<4;j++){
                System.out.print(nums[i][j]);










            }
        }
    }
}
